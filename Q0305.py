# Create a nested tuple
tuple = ((1, 2, 3), (4, 5, 6), (7, 8, 9))
# positive and negative indexes
first = tuple[0][0]
second = tuple[1][1]
last = tuple[-1][-1]
# accessed elements
print("First element:", first)   #1
print("Second element:", second)   #5
print("Last element:", last)        #9









